/// \section intro Program Introduction
/// The <b>MyShape</b> application is meant to show how <i>constructors</i> and <i>destructors</i>
/// are used and/or called within an OO application. In this program we have implemented Shape class.
/// In the shape class, we are setting up name and colour of the Shape. We have also created two more
/// classes - Circle & Square. Circle class has radius attributes of Circle and Square class has
/// sideLength attribute of the Shape. 
/// 
/// As part of A7, we are implementing overlaoded operators - add(+), multiplication(*), assignment(=),
/// and equality check(==) in Circle and Square classes. In order to implement these operators, we have 
/// also implemented "const" for methods so const type objects can access those const methods instead 
/// off regular methods. 
///
/// As part of A8, we created one more class called - CircleSquare which publicly inherits Shape class and
/// implements radius and sideLength. CircleSquare can be used to implement two types of objects - CircleSquare
/// and SquareCircle. In this new class, we also have overlaoded operators - add(+), and assignment(=).
/// 
